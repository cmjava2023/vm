package org.cmjava2023;

public class Main {
    public static void main(String[] args) {
        int i = 4;
        int two = 2;
        // iand
        int i2 = i & 2;
        // ior
        int i3 = i | 2;
        // ishl
        int i4 = two << 1;
        // ishr
        int i5 = two >> 1;
        // iushr
        int i6 = two >>> 1;
        // ixor
        int i7 = i2 ^ 2 ;
    }
}

